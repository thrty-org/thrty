export function compose(...fns: any[]) {
  if (fns.length === 1) fns.unshift((n: any) => n);
  return fns.reduce((f, g) =>
    Object.assign(
      (next: any) =>
        Object.assign(f(g(next)), { actual: next, meta: { ...f.meta, ...g.meta } }),
      { meta: { ...f.meta, ...g.meta } },
    ),
  );
}
